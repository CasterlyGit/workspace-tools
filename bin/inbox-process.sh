#!/bin/bash
BIN_DIR="${BIN_DIR:-$(cd "$(dirname "$0")" && pwd)}"
# inbox-process.sh — handle a markdown ticket's lifecycle.
#
# Two kinds of files trigger this:
#  1. Per-repo ticket files at ~/Documents/Dev/<repo>/inbox/<name>.md
#     - Created  → register a row in _status.md (State: 📝 ready, Agent: —)
#     - Edited   → just refresh state title if the heading changed
#     - Deleted  → close the linked GitHub issue
#  2. The status file at ~/Documents/ObsidianVault/Specs/_status.md
#     - Edited   → parse the Action column for each row; any cell containing
#                  the keyword `go` (case-insensitive) triggers
#                  file-issue + /iterate for that ticket. Cell gets cleared
#                  when status is regenerated.
#
# Per-repo state at <repo>/inbox/.state.json:
#   { "<filename>": { "status": "ready"|"filed"|"done",
#                     "url": "...", "pr_url": "...",
#                     "title": "...", "pr_num": <int>,
#                     "iterated": <bool>, "agent_done": <bool> } }

set -u
PATH=/usr/local/bin:/opt/homebrew/bin:/usr/bin:/bin
LOG=/tmp/inbox.log
VAULT=${VAULT:-$HOME/Documents/ObsidianVault/Specs}
DEV=${DEV:-$HOME/Documents/Dev}
STATUS_FILE="$VAULT/_status.md"
STATUS_HASH_FILE=/tmp/inbox-status.hash
ACTIONS_DIR="$VAULT/_actions"
mkdir -p "$ACTIONS_DIR"

# Generate a .command file that opens in Terminal when double-clicked.
# Args: <output-path> <title-shown-in-terminal> <bash-line>
make_command() {
  local out=$1 title=$2 cmd=$3
  cat > "$out" <<EOF
#!/bin/bash
# Generated by inbox-process.sh — do not hand-edit; regenerated automatically.
echo "▶ $title"
echo
$cmd
echo
echo
echo "(done — close this window when ready)"
read -r -p "press enter to close > "
EOF
  chmod +x "$out"
}

log() { echo "[$(date '+%H:%M:%S')] $*" >> "$LOG"; }

# Extract a `key: value` magic line from the first 10 lines of a ticket body.
# Returns the value lowercased and trimmed, or empty.
extract_magic() {
  local raw=$1 key=$2
  printf '%s\n' "$raw" | head -10 | grep -iE "^[[:space:]]*${key}:" | head -1 \
    | sed -E "s/^[[:space:]]*${key}:[[:space:]]*//I" \
    | tr -d '\r' | tr '[:upper:]' '[:lower:]' | xargs
}

# Strip recognised magic lines from a body so they don't pollute the GitHub
# issue. Magic lines are metadata, not content.
strip_magic_lines() {
  printf '%s\n' "$1" | grep -ivE '^[[:space:]]*(approve|type|priority|pipeline|auto-iterate):[[:space:]]'
}

# Lightly polish stream-of-consciousness ticket content via `claude --print`
# before it gets posted to GitHub. The user types fast and doesn't want their
# raw, typo-laden thoughts showing up on issues / comments verbatim.
#
# Fail-soft: if `claude` is missing or returns nothing within the timeout, we
# fall back to the raw content rather than blocking the pipeline.
#
# Usage:  polished=$(polish_for_github <kind> <<<"$raw")
#   <kind> = issue | comment
polish_for_github() {
  local kind=${1:-issue}
  local raw=$(cat)
  if [ -z "$raw" ]; then echo ""; return 0; fi
  if ! command -v claude >/dev/null 2>&1; then
    echo "$raw"; return 0
  fi
  local instr
  case "$kind" in
    issue)
      instr='Lightly polish a rough ticket the user typed quickly into a clean GitHub issue body. Goals: fix typos, normalize spelling, smooth grammar, expand obvious shorthand. STRICTLY DO NOT: invent acceptance criteria, scope, or constraints they did not write; add headings or sections that were not implied; pad with commentary. Preserve their bullet structure, intent, and tone exactly. Drop a leading "# Title" line if present (used elsewhere). Output ONLY the polished markdown body — no preamble, no closing remark.'
      ;;
    comment)
      instr='Lightly polish a rough follow-up the user typed quickly as a GitHub issue comment. Fix typos, normalize spelling, smooth grammar. STRICTLY DO NOT add headings, structure, or content that was not in the original. Preserve their tone and intent exactly. Output ONLY the polished markdown body — no preamble, no closing remark.'
      ;;
    *)
      echo "$raw"; return 0
      ;;
  esac
  local prompt="$instr

User's raw input is between the <INPUT> tags below. Polish ONLY what is between the tags.

<INPUT>
$raw
</INPUT>"
  local polished
  polished=$(timeout 90 claude --print --dangerously-skip-permissions "$prompt" 2>/dev/null)
  # Trim leading/trailing whitespace
  polished=$(printf '%s' "$polished" | awk 'NR==1 && /^$/{next} {print}' | sed -e ':a' -e '/^$/{$d;N;ba' -e '}')
  if [ -z "$polished" ]; then
    log "polish_for_github($kind): empty AI output, falling back to raw"
    echo "$raw"
  else
    log "polish_for_github($kind): polished ${#raw} → ${#polished} chars"
    echo "$polished"
  fi
}

# ── helpers ─────────────────────────────────────────────────────────────────

# Find the open PR for a given issue's branch (auto/<N>-*) on its repo.
find_pr_for_issue() {
  local repo_full=$1 issue_num=$2
  gh pr list --repo "$repo_full" --state all --json number,url,state,headRefName \
    --jq ".[] | select(.headRefName | startswith(\"auto/$issue_num-\")) | {number, url, state}" 2>/dev/null \
    | head -1
}

# Count how many pipeline stages have completed on the auto branch for this issue.
# Stages commit with prefixes: explore, research, requirements, design, test-plan, implement, integration-test.
stages_done_for_issue() {
  local repo_dir=$1 issue_num=$2
  cd "$repo_dir" 2>/dev/null || return 0
  local branch=$(git branch --list "auto/${issue_num}-*" --format '%(refname:short)' 2>/dev/null | head -1)
  [ -z "$branch" ] && { echo 0; return; }
  git log "$branch" --not main --oneline 2>/dev/null \
    | grep -cE "^[a-f0-9]+ (explore|research|requirements|design|test-plan|implement|integration-test):" \
    || echo 0
}

# Last meaningful line from the iterate log — short, one line, no markdown.
last_iterate_breadcrumb() {
  [ -f /tmp/inbox-iterate.log ] || { echo ""; return; }
  tail -100 /tmp/inbox-iterate.log \
    | grep -vE '^\s*$|^```|^---' \
    | tail -1 \
    | head -c 80
}

regenerate_status() {
  # Write to a temp file then atomic-rename so two concurrent renderers
  # (e.g. multiple orchestrator heartbeats firing at once) can't garble
  # _status.md. mv on a same-filesystem path is atomic on POSIX.
  # Note: macOS BSD mktemp only accepts trailing X's, not X's-then-suffix.
  local _status_tmp=$(mktemp "/tmp/inbox-status-XXXXXX")
  {
    echo "# Tickets"
    echo ""
    echo "_Updated: $(date '+%Y-%m-%d %H:%M:%S')_"
    echo ""
    echo "**Everything is a click.** No typing in cells — every action in the table below is a link that opens Terminal and runs the right command for you."
    echo ""
    echo "**Not updating?** Click to refresh:"
    echo "- 🔄 [refresh status](file://$ACTIONS_DIR/refresh-status.command)"
    echo ""
    echo "## Run latest app"
    echo ""
    echo "Click to pull the latest \`main\` and launch the app:"
    echo ""
    local has_runnable=0
    for project in "$DEV"/*/; do
      [ -d "$project/.git" ] || continue
      local name=$(basename "$project")
      if [ -f "$project/main.py" ] || [ -f "$project/package.json" ]; then
        local cmd_path="$ACTIONS_DIR/run-${name}.command"
        make_command "$cmd_path" "Running $name (latest main)" "${BIN_DIR:-$HOME/bin}/run-app.sh $name"
        echo "- ▶️ [run **$name**](file://$cmd_path)"
        has_runnable=1
      fi
    done
    [ $has_runnable -eq 0 ] && echo "- _(no runnable projects yet)_"
    echo ""
    echo "## Tickets"
    echo ""
    echo "| Project | Tag | Title | State | Action |"
    echo "|---|---|---|---|---|"
    local count=0
    for project in "$DEV"/*/; do
      [ -d "$project/.git" ] || continue
      local name=$(basename "$project")
      local state="$project/inbox/.state.json"
      [ -f "$state" ] || continue
      local owner=$(cd "$project" && gh repo view --json owner --jq .owner.login 2>/dev/null)
      local repo_full="$owner/$name"
      while IFS=$'\x1f' read -r filename status url pr_num iterated agent_done; do
        local title=$(jq -r --arg k "$filename" '.[$k].title // empty' "$state" 2>/dev/null)
        [ -z "$title" ] && title="$filename"
        local tag="—"
        [ -n "$pr_num" ] && [ "$pr_num" != "null" ] && tag="pr-$pr_num"

        # Always pre-make a "go" button for this ticket — its meaning depends on state
        local go_cmd="$ACTIONS_DIR/go-${name}-pr${pr_num}.command"
        make_command "$go_cmd" "go: $name pr-$pr_num" "${BIN_DIR:-$HOME/bin}/inbox-process.sh --go $name $pr_num"

        local state_col="" action_col="—"
        if [ "$status" = "ready" ]; then
          state_col="📝 ready"
          action_col="▶️ [go — file & start agent](file://$go_cmd)"
        elif [ "$status" = "filed" ]; then
          local issue_num=$(echo "$url" | sed 's|.*/issues/||')

          # Is a /iterate process still alive for this ticket? If so, force
          # "working" state regardless of whether a PR exists from a previous run.
          local iter_pid=$(jq -r --arg k "$filename" '.[$k].iterate_pid // empty' "$state")
          local iterating_pid=$(jq -r --arg k "$filename" '.[$k].iterating_pid // empty' "$state")
          local agent_alive=false
          if [ -n "$iter_pid" ] && kill -0 "$iter_pid" 2>/dev/null; then
            agent_alive=true
          elif [ -n "$iter_pid" ]; then
            local TMP=$(mktemp)
            jq --arg k "$filename" 'del(.[$k].iterate_pid) | .[$k].agent_done = true' "$state" > "$TMP" && mv "$TMP" "$state"
          fi
          # New wrapper-based iterating: same liveness logic, different field
          if [ -n "$iterating_pid" ] && kill -0 "$iterating_pid" 2>/dev/null; then
            agent_alive=true
          elif [ -n "$iterating_pid" ]; then
            local TMP=$(mktemp)
            jq --arg k "$filename" 'del(.[$k].iterating, .[$k].iterating_pid) | .[$k].agent_done = true' "$state" > "$TMP" && mv "$TMP" "$state"
          fi

          local pr_info=$(find_pr_for_issue "$repo_full" "$issue_num")
          # Live progress from the orchestrator (state_sink writes these)
          local current_stage=$(jq -r --arg k "$filename" '.[$k].current_stage // empty' "$state" 2>/dev/null)
          local stage_index=$(jq -r --arg k "$filename" '.[$k].stage_index // empty' "$state" 2>/dev/null)
          local total_stages=$(jq -r --arg k "$filename" '.[$k].total_stages // empty' "$state" 2>/dev/null)
          local shape=$(jq -r --arg k "$filename" '.[$k].shape // empty' "$state" 2>/dev/null)
          local last_activity=$(jq -r --arg k "$filename" '.[$k].last_activity // empty' "$state" 2>/dev/null)
          local stage_started_at=$(jq -r --arg k "$filename" '.[$k].stage_started_at // empty' "$state" 2>/dev/null)
          local last_heartbeat=$(jq -r --arg k "$filename" '.[$k].last_heartbeat // empty' "$state" 2>/dev/null)

          if [ -n "$current_stage" ] && [ "$current_stage" != "null" ]; then
            # Orchestrator is mid-stage RIGHT NOW.
            # Build a unicode progress bar: ▰ for done/current, ▱ for pending.
            local bar=""
            if [ -n "$total_stages" ] && [ "$total_stages" != "null" ] && [ "$total_stages" -gt 0 ] 2>/dev/null; then
              local _i=1
              while [ "$_i" -le "$total_stages" ]; do
                if [ "$_i" -le "${stage_index:-0}" ]; then bar="${bar}▰"; else bar="${bar}▱"; fi
                _i=$((_i+1))
              done
            fi

            # Elapsed on current stage — bucketed to ~minutes so the row
            # text doesn't change on every 3s heartbeat (avoids Obsidian
            # snapping the user's scroll to top on each redraw).
            local elapsed=""
            if [ -n "$stage_started_at" ] && [ "$stage_started_at" != "null" ]; then
              local _start=$(date -j -f "%Y-%m-%dT%H:%M:%S" "$stage_started_at" +%s 2>/dev/null)
              if [ -n "$_start" ]; then
                local _now=$(date +%s)
                local _secs=$((_now - _start))
                if [ "$_secs" -lt 60 ]; then
                  elapsed="<1m"
                else
                  elapsed="$((_secs / 60))m"
                fi
              fi
            fi

            # No spinner — see scroll-position commentary above. Liveness
            # comes from the pulse + breadcrumb + elapsed bucket changes.

            # Liveness check — only flip yellow/red when we actually have a
            # heartbeat field (newer orchestrators write it every 3s). For
            # older orchestrators the process-alive check upstream is
            # authoritative; default to green.
            local _pulse="🟢"
            if [ -n "$last_heartbeat" ] && [ "$last_heartbeat" != "null" ]; then
              local _hb=$(date -j -f "%Y-%m-%dT%H:%M:%S" "$last_heartbeat" +%s 2>/dev/null)
              if [ -n "$_hb" ]; then
                local _hb_age=$(( $(date +%s) - _hb ))
                [ "$_hb_age" -gt 30 ] && _pulse="🟡"
                [ "$_hb_age" -gt 90 ] && _pulse="🔴"
              fi
            fi

            local prog="${stage_index:-?}/${total_stages:-?}"
            state_col="${_pulse} [#$issue_num]($url) · ${current_stage} ${bar} (${prog})"
            [ -n "$elapsed" ] && state_col="$state_col · ${elapsed}"
            [ -n "$shape" ] && state_col="$state_col · _${shape}_"
            if [ -n "$last_activity" ] && [ "$last_activity" != "null" ]; then
              local crumb=${last_activity//|/ }
              state_col="$state_col<br>_${crumb}_"
            fi
            action_col="_agent running — wait_"
            echo "| $name | $tag | $title | $state_col | $action_col |"
            count=$((count+1))
            continue
          fi

          if [ "$agent_alive" = "true" ]; then
            # Process alive but no current_stage published — fallback display
            local done_n=$(stages_done_for_issue "$project" "$issue_num")
            local breadcrumb=$(last_iterate_breadcrumb)
            state_col="🟢 [#$issue_num working]($url) · stage $done_n"
            [ -n "$breadcrumb" ] && state_col="$state_col<br>_${breadcrumb}_"
            action_col="_agent running — wait_"
            echo "| $name | $tag | $title | $state_col | $action_col |"
            count=$((count+1))
            continue
          fi

          if [ -n "$pr_info" ]; then
            local prn=$(echo "$pr_info" | jq -r .number)
            local prurl=$(echo "$pr_info" | jq -r .url)
            local prstate=$(echo "$pr_info" | jq -r .state)
            local TMP=$(mktemp)
            jq --arg k "$filename" --arg pu "$prurl" --argjson ad true \
               '.[$k].pr_url = $pu | .[$k].agent_done = $ad' "$state" > "$TMP" && mv "$TMP" "$state"

            # Live amend in progress (set by run-skill-with-state.sh wrapper)
            local amending=$(jq -r --arg k "$filename" '.[$k].amending // false' "$state" 2>/dev/null)
            local amending_pid=$(jq -r --arg k "$filename" '.[$k].amending_pid // empty' "$state" 2>/dev/null)
            if [ "$amending" = "true" ]; then
              # Pid sanity check — clear if dead
              if [ -n "$amending_pid" ] && ! kill -0 "$amending_pid" 2>/dev/null; then
                local TMP=$(mktemp)
                jq --arg k "$filename" 'del(.[$k].amending, .[$k].amending_pid)' "$state" > "$TMP" && mv "$TMP" "$state"
                amending="false"
              fi
            fi
            if [ "$amending" = "true" ]; then
              local started=$(jq -r --arg k "$filename" '.[$k].amending_started_at // empty' "$state" 2>/dev/null)
              local elapsed_s=0 elapsed=""
              if [ -n "$started" ] && [ "$started" != "null" ]; then
                local _start=$(date -j -f "%Y-%m-%dT%H:%M:%S" "$started" +%s 2>/dev/null)
                if [ -n "$_start" ]; then
                  elapsed_s=$(( $(date +%s) - _start ))
                  if [ "$elapsed_s" -lt 60 ]; then elapsed="${elapsed_s}s"; else elapsed="$((elapsed_s / 60))m$((elapsed_s % 60))s"; fi
                fi
              fi

              # Progress + ETA from the skill's progress-tick checkpoints
              local p_step=$(jq -r --arg k "$filename" '.[$k].progress.step // empty' "$state" 2>/dev/null)
              local p_total=$(jq -r --arg k "$filename" '.[$k].progress.total // empty' "$state" 2>/dev/null)
              local p_label=$(jq -r --arg k "$filename" '.[$k].progress.label // empty' "$state" 2>/dev/null)
              local progress_str=""
              local eta_str=""
              if [ -n "$p_step" ] && [ "$p_step" != "null" ] && [ -n "$p_total" ] && [ "$p_total" != "null" ]; then
                # Unicode bar — one cell per step
                local bar="" _i=1
                while [ "$_i" -le "$p_total" ]; do
                  if [ "$_i" -le "$p_step" ]; then bar="${bar}▰"; else bar="${bar}▱"; fi
                  _i=$((_i+1))
                done
                progress_str="${bar} ${p_step}/${p_total}"
                [ -n "$p_label" ] && [ "$p_label" != "null" ] && progress_str="$progress_str (${p_label})"
                # ETA: linear projection from elapsed/step
                if [ "$p_step" -gt 0 ] 2>/dev/null && [ "$elapsed_s" -gt 0 ]; then
                  local _remaining_steps=$((p_total - p_step))
                  local _per_step=$((elapsed_s / p_step))
                  local _eta_s=$((_per_step * _remaining_steps))
                  if [ "$_eta_s" -le 0 ]; then
                    eta_str="finishing up"
                  elif [ "$_eta_s" -lt 60 ]; then
                    eta_str="~${_eta_s}s left"
                  else
                    eta_str="~$((_eta_s / 60))m$((_eta_s % 60))s left"
                  fi
                fi
              fi

              state_col="🟡 [PR #$prn]($prurl) · amending"
              [ -n "$elapsed" ] && state_col="$state_col · ${elapsed} elapsed"
              [ -n "$eta_str" ] && state_col="$state_col · ${eta_str}"
              [ -n "$progress_str" ] && state_col="$state_col<br>${progress_str}"
              action_col="_agent running in Terminal — watch the window_"
              echo "| $name | $tag | $title | $state_col | $action_col |"
              count=$((count+1))
              continue
            fi

            if [ "$prstate" = "MERGED" ]; then
              state_col="🎉 [PR #$prn merged]($prurl)"
              local run_cmd="$ACTIONS_DIR/run-${name}.command"
              make_command "$run_cmd" "Running $name (latest main, includes PR #$prn)" "${BIN_DIR:-$HOME/bin}/run-app.sh $name"
              action_col="▶️ [run app](file://$run_cmd) · then delete ticket file"
            elif [ "$prstate" = "CLOSED" ]; then
              state_col="❌ [PR #$prn closed unmerged]($prurl)"
              action_col="_delete file or re-open in GitHub_"
            else
              state_col="✅ done → [PR #$prn]($prurl)"
              local test_cmd="$ACTIONS_DIR/try-${name}-pr${prn}.command"
              local merge_cmd="$ACTIONS_DIR/merge-${name}-pr${prn}.command"
              make_command "$test_cmd" "Try $name PR #$prn — runs tests + launches the app on this PR" "${BIN_DIR:-$HOME/bin}/pr-test.sh $name $prn"
              make_command "$merge_cmd" "Merging $name PR #$prn (squash + delete branch)" "cd ${DEV:-$HOME/Documents/Dev}/$name && gh pr merge $prn --squash --delete-branch"
              local feedback_cmd="$ACTIONS_DIR/feedback-${name}-pr${prn}.command"
              make_command "$feedback_cmd" "Amend $name PR #$prn — read latest comment, implement, push" "${BIN_DIR:-$HOME/bin}/run-skill-with-state.sh '${DEV:-$HOME/Documents/Dev}/$name/inbox/.state.json' '$filename' amending '${DEV:-$HOME/Documents/Dev}/$name' claude --dangerously-skip-permissions '/pr-amend $prurl'"
              action_col="▶️ [try PR app](file://$test_cmd) · 💬 [comment on PR]($prurl#comments) · ↻ [re-run with feedback](file://$feedback_cmd) · ✅ [merge](file://$merge_cmd)"
            fi
          else
            local agent_done_now=$(jq -r --arg k "$filename" '.[$k].agent_done // false' "$state" 2>/dev/null)
            if [ "$iterated" = "true" ] && [ "$agent_done_now" = "false" ]; then
              state_col="🟢 [#$issue_num working]($url)"
              action_col="_agent running — wait_"
            elif [ "$iterated" = "true" ] && [ "$agent_done_now" = "true" ]; then
              local done_n=$(stages_done_for_issue "$project" "$issue_num")
              state_col="⚠️ [#$issue_num agent stopped early]($url) · stage $done_n/7"
              action_col="↻ [re-run agent](file://$go_cmd)"
            else
              state_col="✅ [#$issue_num filed]($url)"
              action_col="▶️ [go — start agent](file://$go_cmd)"
            fi
          fi
        fi
        echo "| $name | $tag | $title | $state_col | $action_col |"
        count=$((count+1))
      done < <(jq -r 'to_entries[] | [.key, (.value.status // "?"), (.value.url // ""), (.value.pr_num // ""|tostring), (.value.iterated // false|tostring), (.value.agent_done // false|tostring)] | join("")' "$state" 2>/dev/null)
    done
    [ $count -eq 0 ] && echo "| — | — | _no tickets — create a markdown file inside any project folder_ | — | — |"
    echo ""
    echo "---"
    echo ""
    echo "## Legend"
    echo ""
    echo "**State column:**"
    echo ""
    echo "| Symbol | Meaning | What's happening |"
    echo "|---|---|---|"
    echo "| 📝 | **ready** | Ticket file exists. No GitHub issue yet, no agent. Waiting for your \`go\`. |"
    echo "| ✅ #N | **filed** | GitHub issue created, no agent yet. Type \`go\` to start the agent. |"
    echo "| 🟢 #N working | **agent working** | Agent is actively running the SDD pipeline. No action from you. |"
    echo "| ✅ done → PR #N | **PR open** | Agent finished. Review the PR in GitHub. |"
    echo "| 🎉 PR #N merged | **shipped** | You merged the PR. Delete the ticket file to clean up. |"
    echo "| ❌ PR #N closed | **PR closed unmerged** | You closed without merging. Delete the file or re-open. |"
    echo ""
    echo "**Action column — every action is a clickable link, no typing:**"
    echo ""
    echo "| State of row | What the click does |"
    echo "|---|---|"
    echo "| 📝 ready → ▶️ go | Files the GitHub issue AND starts the agent. |"
    echo "| ✅ filed (idle) → ▶️ go | Starts the agent on the existing issue. |"
    echo "| 🟢 working | _no link — agent already running, just wait_ |"
    echo "| ✅ done → ▶️ try PR app | Opens Terminal: checks out the PR's branch, runs a quick test pass, then **launches the app on that branch** so you can verify the change manually. Test, then: |"
    echo "| ✅ done (after testing) → 💬 comment on PR | Post feedback / requested changes as a **comment on the PR**. The orchestrator picks it up and re-runs implement + tests with just your delta. No local file edits needed. |"
    echo "| ✅ done → ✅ merge | Opens Terminal: \`gh pr merge --squash --delete-branch\` for that PR. |"
    echo "| 🎉 merged → ▶️ run app | Pulls latest main and launches the app. |"
    echo ""
    echo "**When a PR is ready, the Action cell shows two commands you can copy into Terminal:**"
    echo ""
    echo "- **\`pr-test.sh <repo> <pr#>\`** — checks out the PR's branch, runs its tests, leaves you ready to launch the app and verify the fix manually."
    echo "- **\`gh pr merge <pr#> --squash\`** — merge the PR if you're satisfied. The branch and ticket file get cleaned up afterwards."
    echo ""
    echo "**Outside the table:**"
    echo ""
    echo "| You do | What happens |"
    echo "|---|---|"
    echo "| Create \`<repo>/<name>.md\` in a project folder | Adds a 📝 ready row. |"
    echo "| Delete the ticket file | Closes the GitHub issue and removes the row. |"
    echo "| Merge a PR in GitHub | Row moves to 🎉 merged on the next refresh. |"
    echo ""
    echo "**Default behavior:** nothing fires until you type \`go\`. The Action column is the only control surface."
  } > "$_status_tmp"

  # Skip rewrite if the new content matches the current file. Without this,
  # every 3s heartbeat from a running orchestrator triggers a no-op write,
  # Obsidian re-renders, and the user's scroll snaps back to the top.
  if [ -f "$STATUS_FILE" ] && cmp -s "$_status_tmp" "$STATUS_FILE"; then
    rm -f "$_status_tmp"
    return 0
  fi

  # In-place rewrite (truncate + write) instead of `mv`, so the inode is
  # preserved. Obsidian treats an inode swap as "file replaced" and re-renders
  # from scratch (snapping scroll to top); an in-place modify is handled as a
  # normal edit and preserves scroll in Source / Live Preview mode.
  cat "$_status_tmp" > "$STATUS_FILE"
  rm -f "$_status_tmp"

  # Track our own write so the watcher doesn't re-process its own output
  shasum "$STATUS_FILE" | awk '{print $1}' > "$STATUS_HASH_FILE"
}

# Trigger the agent for a given (repo, ticket-filename).
# Handles three states with the SAME `go` action:
#   ready      → file issue + /iterate
#   filed      → /iterate (issue already exists)
#   done (agent_done=true) → post the file content as an issue comment ("amend"),
#                            then /iterate again so the agent picks up new work.
fire_agent() {
  local repo_dir=$1 filename=$2
  local repo_name=$(basename "$repo_dir")
  local owner=$(cd "$repo_dir" && gh repo view --json owner --jq .owner.login 2>/dev/null)
  local repo_full="$owner/$repo_name"
  local state="$repo_dir/inbox/.state.json"
  local fpath="$repo_dir/inbox/$filename"
  [ ! -f "$fpath" ] && { log "fire_agent: missing ticket file $fpath"; return 1; }

  local current_status=$(jq -r --arg k "$filename" '.[$k].status // empty' "$state")
  local agent_done=$(jq -r --arg k "$filename" '.[$k].agent_done // false' "$state")
  local raw=$(cat "$fpath")
  local url=""

  # Parse magic lines BEFORE stripping them. `type:` controls the GitHub label
  # (default `bug` — most inbox tickets are small fixes, not big features).
  # `pipeline:` overrides the orchestrator's shape inference if set.
  local magic_type=$(extract_magic "$raw" "type")
  local magic_pipeline=$(extract_magic "$raw" "pipeline")
  case "$magic_type" in bug|feature|polish|chore|docs|security) ;; *) magic_type="bug" ;; esac
  case "$magic_pipeline" in instant|fast|full|feature|bugfix|bug|quickfix|polish|chore|greenfield) ;; *) magic_pipeline="" ;; esac
  local clean_raw=$(strip_magic_lines "$raw")

  # Default empty — fresh tickets don't need a rerun-from. The amend path
  # below sets this for already-filed tickets.
  local rerun_from=""

  if [ "$current_status" = "ready" ]; then
    # ── First-time file ─────────────────────────────────────────────────────
    local title=$(jq -r --arg k "$filename" '.[$k].title // empty' "$state")
    [ -z "$title" ] && title=$(basename "$filename" .md | sed 's/[-_]/ /g')

    local pr_num=$(jq -r --arg k "$filename" '.[$k].pr_num // empty' "$state")
    [ -z "$pr_num" ] && pr_num=$(jq '[.[].pr_num // 0] | (max // 0) + 1' "$state")

    local polished_body=$(printf '%s' "$clean_raw" | polish_for_github issue)
    url=$(gh issue create --repo "$repo_full" --title "$title" --body "$polished_body" --label "$magic_type" 2>&1 | tail -1)
    case "$url" in https://*) ;; *) log "fire_agent FAIL filing $repo_full: $url"; return 1 ;; esac

    local content_hash=$(echo "$raw" | shasum | awk '{print $1}')
    local TMP=$(mktemp)
    jq --arg k "$filename" --arg t "$title" --arg u "$url" --argjson p "$pr_num" --arg h "$content_hash" \
       '.[$k] = {status:"filed", url:$u, title:$t, pr_num:$p, iterated:false, agent_done:false, content_hash:$h}' "$state" > "$TMP" && mv "$TMP" "$state"

    local proj=$(gh project list --owner @me --format json --jq ".projects[] | select(.title==\"$repo_name\") | .number" 2>/dev/null)
    [ -n "$proj" ] && gh project item-add "$proj" --owner @me --url "$url" >/dev/null 2>&1
    local hub=$(gh project list --owner @me --format json --jq '.projects[] | select(.title=="Workspace") | .number' 2>/dev/null)
    [ -n "$hub" ] && gh project item-add "$hub" --owner @me --url "$url" >/dev/null 2>&1
    log "FILED $repo_full pr-$pr_num: $title → $url"
  else
    url=$(jq -r --arg k "$filename" '.[$k].url // empty' "$state")
    [ -z "$url" ] && { log "fire_agent: no URL for $filename"; return 1; }

    # ── Amend logic ────────────────────────────────────────────────────────
    # Two amend modes:
    #   (a) Content UNCHANGED (user clicked "go" again on a done ticket
    #       without editing it) — no-op. There's no new work to do; firing
    #       the orchestrator would just produce duplicate artifact commits
    #       with no code change. Log and return.
    #   (b) Content CHANGED (user edited the ticket file) — post the new
    #       content as a comment on the issue, then fire the orchestrator
    #       with --amend so it clears ALL artifacts for the current shape
    #       and re-runs from stage 1 with the new ticket text. This avoids
    #       the previous bug where rerun-from=implement ran against a
    #       stale DESIGN.md so amend-time ACs became artifact-only commits.
    local current_hash=$(echo "$raw" | shasum | awk '{print $1}')
    local prev_hash=$(jq -r --arg k "$filename" '.[$k].content_hash // empty' "$state")
    local amend_arg=""

    if [ -n "$prev_hash" ] && [ "$prev_hash" = "$current_hash" ]; then
      log "AMEND-NOOP $url — content unchanged since last run, skipping fire"
      osascript -e "display notification \"no changes — edit the ticket then click go again\" with title \"$repo_full\" sound name \"Pop\"" 2>/dev/null || true
      return 0
    fi

    if [ -n "$prev_hash" ] && [ "$prev_hash" != "$current_hash" ]; then
      local polished_comment=$(printf '%s' "$clean_raw" | polish_for_github comment)
      gh issue comment "$url" --body "## Updated by user via inbox

$polished_comment" >/dev/null 2>&1
      log "AMEND $url — posted polished comment, full re-run with new ticket"
      amend_arg="--amend"
    fi

    # Persist new content_hash + reset agent_done so the status row flips
    # back to 🟢 working.
    local TMP=$(mktemp)
    jq --arg k "$filename" --arg h "$current_hash" --arg aa "$amend_arg" \
       '.[$k].agent_done = false | .[$k].content_hash = $h | .[$k].last_amend = $aa' \
       "$state" > "$TMP" && mv "$TMP" "$state"
  fi

  # Launch /iterate-fast in a visible Terminal window. Skill = a slash command
  # under ~/.claude/commands/. The user watches the agent stream live; can
  # cancel with Esc/ctrl-C; no opaque background daemon, no .state.json
  # bookkeeping race.
  local launcher_file="$ACTIONS_DIR/auto-iterate-${repo_name}-$(echo "$url" | sed 's|.*/||').command"
  make_command "$launcher_file" "iterate $repo_full — $url" \
    "${BIN_DIR:-$HOME/bin}/run-skill-with-state.sh '$state' '$filename' iterating '$repo_dir' claude --dangerously-skip-permissions '/iterate-fast $url'"
  open "$launcher_file"
  local TMP=$(mktemp)
  jq --arg k "$filename" \
     '.[$k].iterated = true | .[$k].agent_done = false' \
     "$state" > "$TMP" && mv "$TMP" "$state"
  log "ITERATE $url (skill=/iterate-fast in Terminal)"
  osascript -e "display notification \"agent started — $url\" with title \"go: $repo_full\" sound name \"Glass\"" 2>/dev/null || true
}

# Parse the status file for "go" actions and fire them.
process_status_file_edits() {
  [ -f "$STATUS_FILE" ] || return 0

  # Skip if hash matches our own last write — i.e. this is a regeneration, not a user edit
  if [ -f "$STATUS_HASH_FILE" ]; then
    local current_hash=$(shasum "$STATUS_FILE" | awk '{print $1}')
    local last_hash=$(cat "$STATUS_HASH_FILE")
    [ "$current_hash" = "$last_hash" ] && return 0
  fi

  # Walk the table. Format: | project | tag | title | state | action |
  # We trigger when the action cell (col 5) trimmed lowercase is "go".
  local fired=0
  while IFS='|' read -r _ project tag title _ action _; do
    project=$(echo "$project" | xargs)        # trim
    tag=$(echo "$tag" | xargs)
    action=$(echo "$action" | xargs)
    [ -z "$project" ] || [ "$project" = "Project" ] && continue
    case "$tag" in pr-*) ;; *) continue ;; esac
    local action_lc=$(echo "$action" | tr '[:upper:]' '[:lower:]')
    [ "$action_lc" != "go" ] && continue

    # Find the ticket file by (project, pr_num)
    local pr_num="${tag#pr-}"
    local repo_dir="$DEV/$project"
    local state="$repo_dir/inbox/.state.json"
    [ ! -f "$state" ] && continue
    local filename=$(jq -r --argjson p "$pr_num" 'to_entries[] | select(.value.pr_num == $p) | .key' "$state" 2>/dev/null | head -1)
    [ -z "$filename" ] && { log "process_status: no ticket with pr_num=$pr_num in $project"; continue; }

    log "GO from status file → $project/$filename"
    fire_agent "$repo_dir" "$filename"
    fired=$((fired+1))
  done < "$STATUS_FILE"

  # Always regenerate to clear the action cells
  regenerate_status
  [ $fired -gt 0 ] && log "status: fired $fired ticket(s)"
}

# ── main entry ──────────────────────────────────────────────────────────────

# Direct-trigger mode: invoked by the per-ticket "go" .command buttons.
# Usage: inbox-process.sh --go <repo> <pr_num>
if [ "${1:-}" = "--go" ]; then
  GO_REPO="${2:-}"
  GO_PR_NUM="${3:-}"
  [ -z "$GO_REPO" ] || [ -z "$GO_PR_NUM" ] && { echo "usage: $0 --go <repo> <pr_num>"; exit 1; }
  GO_DIR="$DEV/$GO_REPO"
  GO_STATE="$GO_DIR/inbox/.state.json"
  [ ! -f "$GO_STATE" ] && { echo "✗ no state file for $GO_REPO"; exit 1; }
  GO_FILENAME=$(jq -r --argjson p "$GO_PR_NUM" 'to_entries[] | select(.value.pr_num == $p) | .key' "$GO_STATE" 2>/dev/null | head -1)
  [ -z "$GO_FILENAME" ] && { echo "✗ no ticket pr-$GO_PR_NUM in $GO_REPO"; exit 1; }
  echo "▶ go: $GO_REPO/$GO_FILENAME"
  fire_agent "$GO_DIR" "$GO_FILENAME"
  regenerate_status
  exit 0
fi

FILE="${1:-}"
if [ -z "$FILE" ]; then
  regenerate_status
  exit 0
fi

# Status file edits go through their own handler
if [ "$FILE" = "$STATUS_FILE" ]; then
  process_status_file_edits
  exit 0
fi

# Otherwise: a ticket file change/create/delete.
PARENT=$(dirname "$FILE")
[ "$(basename "$PARENT")" != "inbox" ] && exit 0

# Debounce 2s per file
DEBOUNCE_DIR=/tmp/inbox-debounce
mkdir -p "$DEBOUNCE_DIR"
DEBOUNCE_KEY=$(echo "$FILE" | shasum | awk '{print $1}')
DEBOUNCE_FILE="$DEBOUNCE_DIR/$DEBOUNCE_KEY"
NOW=$(date +%s)
if [ -f "$DEBOUNCE_FILE" ]; then
  LAST=$(cat "$DEBOUNCE_FILE")
  [ $((NOW - LAST)) -lt 2 ] && exit 0
fi
echo "$NOW" > "$DEBOUNCE_FILE"

REPO_DIR=$(dirname "$PARENT")
REPO_NAME=$(basename "$REPO_DIR")
[ ! -d "$REPO_DIR/.git" ] && exit 0

BASENAME=$(basename "$FILE")
case "$BASENAME" in README.md|.*|_*) exit 0 ;; esac
[[ "$BASENAME" != *.md ]] && exit 0

STATE="$REPO_DIR/inbox/.state.json"
[ -f "$STATE" ] || echo "{}" > "$STATE"

CURRENT_STATUS=$(jq -r --arg k "$BASENAME" '.[$k].status // empty' "$STATE")

# DELETE
if [ ! -f "$FILE" ]; then
  if [ -z "$CURRENT_STATUS" ]; then regenerate_status; exit 0; fi
  URL=$(jq -r --arg k "$BASENAME" '.[$k].url // empty' "$STATE")
  if [ "$CURRENT_STATUS" = "filed" ] && [ -n "$URL" ]; then
    gh issue close "$URL" --comment "Marked done — note deleted from inbox." 2>&1 | sed 's/^/  /' >> "$LOG"
    log "CLOSED $URL (file deleted)"
  fi
  TMP=$(mktemp); jq --arg k "$BASENAME" 'del(.[$k])' "$STATE" > "$TMP" && mv "$TMP" "$STATE"
  regenerate_status; exit 0
fi

# CREATE / EDIT
RAW=$(cat "$FILE")

# Title: first H1 if present, else cleaned-up filename
FIRST_LINE=$(echo "$RAW" | head -1 | tr -d '\r')
if [[ "$FIRST_LINE" =~ ^#[[:space:]]+(.+)$ ]]; then
  TITLE="${BASH_REMATCH[1]}"
else
  TITLE=$(basename "$FILE" .md | sed 's/[-_]/ /g')
fi

if [ -z "$CURRENT_STATUS" ]; then
  # Brand new — assign pr_num, register as "ready"
  NEXT_PR=$(jq '[.[].pr_num // 0] | (max // 0) + 1' "$STATE")
  TMP=$(mktemp)
  jq --arg k "$BASENAME" --arg t "$TITLE" --argjson p "$NEXT_PR" \
     '.[$k] = {status:"ready", url:null, title:$t, pr_num:$p, iterated:false, agent_done:false}' "$STATE" > "$TMP" && mv "$TMP" "$STATE"
  log "READY $REPO_NAME pr-$NEXT_PR — \"$TITLE\""
  osascript -e "display notification \"$TITLE\" with title \"Ticket pr-$NEXT_PR ready — $REPO_NAME\" subtitle \"open _status.md and type 'go' to start agent\"" 2>/dev/null || true
  regenerate_status
  exit 0
fi

# Existing ticket — refresh title only if it changed
PREV_TITLE=$(jq -r --arg k "$BASENAME" '.[$k].title // empty' "$STATE")
if [ "$PREV_TITLE" != "$TITLE" ]; then
  TMP=$(mktemp); jq --arg k "$BASENAME" --arg t "$TITLE" '.[$k].title = $t' "$STATE" > "$TMP" && mv "$TMP" "$STATE"
  regenerate_status
fi
exit 0
